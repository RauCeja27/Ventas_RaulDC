public class ActuInventario {
    
    private Integer numero_de_factura;
    
    private String fecha_de_compra;
    
    private Integer cantidad;
    
    private Integer id;
    

    public Integer getNumero_de_factura() {
        return numero_de_factura;
    }

    public void setNumero_de_factura(Integer numero_de_factura) {
        this.numero_de_factura = numero_de_factura;
    }

    public String getFecha_de_compra() {
        return fecha_de_compra;
    }

    public void setFecha_de_compra(String fecha_de_compra) {
        this.fecha_de_compra = fecha_de_compra;
    }

    public Integer getCantidad() {
        return cantidad;
    }

    public void setCantidad(Integer cantidad) {
        this.cantidad = cantidad;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
    
    
  
    
    
}
